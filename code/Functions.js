// Function declaration

function sum(a, b) {
    return a + b;
}

console.log(sum(2, 3));

// Function expression

function sum2(a, b) {
    return a + b;
}

console.log(sum2(4, 3));

// Arrow function

const sum3 = (a, b) => a + b; // If the function has only one line, the return statement is implicit

console.log(sum3(5, 3));